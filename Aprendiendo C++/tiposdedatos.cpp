//Tipos de datos basicos de c++

//Importar libreria input output
#include<iostream>

//Seleccionar namespace std para utilizar cout y cin
using namespace std;

int main(){
    //Tipo de dato entero
    int entero = 15;
    cout<<entero<<endl;

    //Tipo de danto flotante
    float flotante = 15.5;
    cout<<flotante<<endl;

    //Tipo de dato doble
    double doble = 25.3;
    cout<<doble<<endl;

    //Tipo de dato caracter
    char caracter = 'A';
    cout<<caracter<<endl;

    //Tipo de dato booleano
    bool booleano = true;
    cout<<booleano<<endl;

    //Tipo de dato cadena de caracteres (string)
    string cadena = "Mi nombre es Adrian!";
    cout<<cadena<<endl;

    return 0;
}
